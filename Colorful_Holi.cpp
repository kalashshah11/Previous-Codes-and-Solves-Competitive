ll calcSubArrayXORSum(vector<int> arr) {
	int n = arr.size();
	ll sum = 0;
	ll multiplier = 1;
	// ll mod = 1000000007;
	for (int i = 0; i < 30; i++) {
		int oddCount = 0;
		bool isOdd = 0;
		for (int j = 0; j < n; j++) {
			if ((arr[j] & (1LL << i)) > 0)
				isOdd = (!isOdd);
			if (isOdd)
				oddCount++;
		}
		for (int j = 0; j < n; j++) {
			sum += (multiplier * oddCount);
			sum = (sum%mod + mod)%mod;
			if ((arr[j] & (1 << i)) > 0)
				oddCount = (n - j - oddCount);
		}
		multiplier *= 2;
		multiplier = multiplier%mod;
	}
	return (sum%mod + mod)%mod;
}
